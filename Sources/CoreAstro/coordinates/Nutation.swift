import CoreMeasure

private func nutationalArguments(T: TimeInterval) -> (D: Angle, M: Angle, Mprime: Angle, F: Angle, 立: Angle) {
    let T1 = T.scalarValue
    let T2 = T1 * T1
    let T3 = T2 * T1
    let D = 297.85036 + 445267.111480 * T1 - 0.0019142 * T2 + T3 / 189474
    let M = 357.52772 + 35999.050340 * T1 - 0.0001603 * T2 - T3 / 300000
    let Mprime = 134.96298 + 477198.867398 * T1 + 0.0086972 * T2 + T3 / 56250
    let F = 93.27191 + 483202.017538 * T1 - 0.0036825 * T2 + T3 / 327270
    let 立 = 125.04452 - 1934.136261 * T1 + 0.0020708 * T2 + T3 / 450000
    return (
        D: try! Angle(D, unit: .degree),
        M: try! Angle(M, unit: .degree),
        Mprime: try! Angle(Mprime, unit: .degree),
        F: try! Angle(F, unit: .degree),
        立: try! Angle(立, unit: .degree)
    )
}

